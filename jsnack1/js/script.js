var num1 = prompt("Inserisci il primo numero: ");
var num2 = prompt("Inserisci il secondo numero: ");
if (num1 > num2) {
  alert("Il primo numero che hai inserito è il maggiore!");
} else if (num1 < num2) {
  alert("Il secondo numero che hai inserito è il maggiore!");
} else {
  alert("I due numeri inseriti sono uguali!");
}
